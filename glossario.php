          <div class="row">
            <div class="col-sm-12 col-md-12">
              <dl>
                <h5>A</h5>
                <dt>Acesso</dt>
                <dd>disponibilidade da informação - registos e arquivos - para consulta, assegurados por legislação que protege este direito.</dd>

                <dt>API</dt>
                <dd>acrónimo de Application Programming Interface, em português, Interface de Programação de Aplicações. No campo dos dados refere-se a um protocolo disponibilizado por uma determinada fonte, para que programas e aplicações possam aceder e usar directamente os dados disponíveis. Uma aplicação pode enviar um pedido à API para uma informação específica, por exemplo, o utilizador clica num ponto no mapa e a API devolve informação sobre esse ponto. Isto permite que as aplicações possam aceder às partes de informação que precisam sem terem de descarregar o conjunto completo de dados (juntar exemplo de um pedido a API). As vantagens desta forma de operar são eficiência e acesso a informação actualizada. As desvantagens são a necessidade constante de acesso à rede e dependência da API.</dd>


                <h5>B</h5>
                <dt>Base de dados</dt>
                <dd>qualquer conjunto organizado de dados pode ser considerado uma base de dados, sendo por isso sinónimo de conjunto(s) de dados; um sistema de software para processar e gerir dados, incluindo características  que permitam estender ou atualizar, transformar e consultar os dados. Exemplo: fonte aberta PostgreSQL, e proprietário Microsoft Access.</dd>


                <h5>C</h5>
                <dt>Cidadãos</dt>
                <dd>em geral, ao longo do glossário, cidadãos é usado em sentido alaragado, incluindo todos os habitantes de um país ou localidade. Existe uma preocupação compreensível que o termo posssa ser usado para excluir determinados grupos sem direito de voto e/ou não naturais de um país, incluindo crianças, adolescentes, migrantes e refugiados.</dd>


                <dt>Confidencialidade</dt>
                <dd>a qualidade ou propriedade da privacidade ou segredo  agregada a determinada informação e/ou registos, que limitam o acesso a estes.</dd>
                <dt>Conjunto(s) dados</dt>
                <dd>em relação aos serviços públicos, estes dados tipicamente são recolhidos como derivado da distribuição. Isto inclui, por exemplo, conjuntos de dados de serviços públicos; dados de satisfação dos utilizadores, e do desempenho dos prestadores de serviços.</dd>
                <dt>Conversão</dt>
                <dd>processo automático de ler os dados num formato específico e emitir os mesmos dados num formato diferente, tornando assim os dados acessíveis a uma gama mais abrangente de aplicações.</dd>

                <h5>D</h5>
                <dt>Dados</dt>
                <dd>Caracteres, números, ou símbolos reunidos por computação, análise estatística ou referência; informação em bruto.</dd>
                <dt>Dados abertos</dt>
                <dd>dados que podem ser livremente usados, reutilizados e redistribuídos por qualquer pessoa.</dd>
                <dt>Dados Abertos Governamentais</dt>
                <dd>Dados abertos relativos ao governo. De forma geral, refere-se a informação recolhida no decorrer do funcionamento quotidiano das instituições governamentais, respeitando a privacidade individual e informação comercial sensível.</dd>
                <dt>Dados em massa (Bulk)</dt>
                <dd>os dados estão disponíveis em massa se for possível, a qualquer utilizador, descarregar um conjunto de dados integralmente e com facilidade. Ao invés, não são dados em massa quando os utilizadores tem o acesso restringido a uma parte dos dados (exemplo, limite a um máximo de 200 resultados numa pesquisa), implicando maior esforço - vários acessos ao site - ou mais tempo para aceder ao conjunto de dados completo. O acesso em massa é uma premissa para dados abertos.</dd>
                <dt>Dados interligados</dt>
                <dd>um método de publicar dados estruturados que melhora a utilidade da informação subjacente que promove a partilha, a conectividade e a  divulgação dos dados.</dd>
                <dt>Direitos de autor</dt>
                <dd>direito legal sobre propriedade intelectual (e. g. um livro) que pertence a um autor. Enquanto dados individuais (factos) não podem ser sujeitos a direitos de autor, uma base de dados em geral será protegida pelos direitos de autor na selecção e disposição de dados presente nesta. A União Europeia separata 'direitos de bases de dados' protegendo bases de dados onde existe um esforço na 'obtenção' dos dados. Um proprietário de direitos de autor pode usar uma licença para garantir a protecção de outras pessoas no material protegido, sujeito a restrições específicas.</dd>

                <h5>I</h5>
                <dt>Informação</dt>
                <dd>Interpretação e análise de dados que, quando contextualizada, constitui valor acrescentado, mensagem e significado.</dd>
                <dt>Informação classificada</dt>
                <dd>informação à qual o acesso têm restrições impostas por um governo no interesse da segurança nacional.</dd>
                <dt>Informação Pública</dt>
                <dd>informação compilada por instituições públicas, sobre as mesmas.</dd>

                <h5>H</h5>
                <dt>Hacking Cívico</dt>
                <dd>construção de ferramentas e comunidades, por norma online, que dá resposta a problemas sociais ou cívicos. Exemplo podem ser ferramentas que ajudam os utilizadores a conhecerem pessoas localmente com interesses comuns, a reportarem problemas infraestruturais ao seu parlamento local, ou colaborarem para limparem lixo na sua vizinhança. Os dados abertos de dimensão local dão extremamente úteis para os projectos de hacking cívico.</dd>

                <h5>L</h5>
                <dt>Liberdade de informação (FOI)</dt>
                <dd>enquadramento legal que estabelece as políticas de disseminação ou divulgação de informação governamental (também acesso à informação, direito à informação).</dd>
                <dt>Legislação de protecção de dados</dt>
                <dd>não acerca da protecção dos dados, mas acerca da protecção do direito dos cidadãos viverem sem medo que a que a sua informação privada seja tornada pública. A legislação protege a privacidade, por exemplo a informação acerca do estatuto económico, de saúde e posição política pessoal.</dd>

                <h5>M</h5>
                <dt>Mecanicamente legível</dt>
                <dd>formatos que são mecanicamente legíveis são formatos que permitem a extração fácil de dados com programas de computador. Ficheiros PDF não são mecanicamente legíveis. Os computadores podem apresentar bem o texto, mas tem imensa dificuldade em compreender o contexto em que o texto se enquadra.</dd>
                <dt>Megadados (Big Data)</dt>
                <dd>Uma colecção de dados tão extensa que não pode ser guardada, transmitida ou processada por meios manuais. A disponibilização crescente de datasets deste género (por exemplo, conjuntos de informação meterológica de um período extenso de tempo) deu origem ao desenvolvimento de linguagens de programação, arquitectura de informação e tecnologias especializadas.</dd>
                <dt>Metadados</dt>
                <dd>"Dados sobre dados" - metadados são dados descritivos ou estruturais que descrevem o conteúdo dos dados ou informação associada. Exemplo: horário e posição geográfica de determinada fotografia.</dd>

                <h5>P</h5>
                <dt>Padrão aberto</dt>
                <dd>definição técnica, refere-se ao uso de formatos interoperáveis, desenvolvidos independentemente de um programa de computador específico. Por exemplo, publicação de informação no formato padrão <a href="https://pt.wikipedia.org/wiki/Comma-separated_values">CSV</a> em vez do uso do formato proprietário de folha de cálculo do XLS, desenvolvido pela Microsoft.</dd>
                <dt>Proteção de dados</dt>
                <dd>protecção legal do direitos dos indivíduos em relação à recolha, armazenamento e processamento automático de dados pessoais em formato mecanicamente legível e a divulgação desses dados.</dd>

                <h5>R</h5>
                <dt>Responsabilização</dt>
                <dd>relação institucionalizada (i. e. regular, estabelecida, aceite) entre diferentes actores que pode ser formal ou informal. É útil pensar uma relação de responsabilização como tendo quatro fases: configuração padrão, investigação, resposta e sanção.</dd>
                <dt>Reutilização</dt>
                <dd>é raro que os dados recolhidos por um motivo especifico não tenham outras aplicações. Felizmente, os dados são um recurso infinito; uma vez reunidos, independentemente do motivo, podem ser reutilizados vezes sem conta, em meios não considerados quando recolhidos inicialmente. Para serem reutilizados os proprietários dos dados devem disponibilizá-los sob licença aberta que permita claramente a reutilização.</dd>

                <h5>S</h5>
                <dt>Sociedade Civil</dt>
                <dd>o glossário usa 'organização da sociedade civil' de forma abrangente para referir um conjunto alargado de organizações não-governamentais e sem fins lucrativos que têm uma presença na vida pública, expressando os interesses e valores dos seus membros ou de outros. Esta pode incluir organizações não-governamentais (ONGs), grupos comunitários, sindicatos, grupos indígenas, organizações religiosas, profissionais e dos meios de comunicação social.</dd>

                <h5>T</h5>
                <dt>Transparência</dt>
                <dd>os governos e outras organizações são consideradas transparentes quando o seu trabalho e processo de tomada de decisão é claro, propriamente documentado e aberto ao escrutínio.  Processo de publicação regular e organização da informação disponibilizada, assegurando que esta está correcta e actualizada. Transparência é um dos aspectos do governo aberto. Um aumento da transparência é um dos benefícios dos dados abertos.</dd>

              </dl>

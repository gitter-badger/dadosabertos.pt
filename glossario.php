          <!-- Glossário -->
          <div class="row">
            <div class="col-sm-12 col-md-12" >
              <h4>Glossário</h4>
              <p>O glossário é composto por termos recorrentes na área dos dados abertos, cuja definição é importante para nos ajudar a compreender com clareza as questões abordadas.</p>
              <p>Os termos abaixo indicados foram recolhidos do <a href="http://opendatahandbook.org/glossary/en">Open Data Handbook</a> e do <a href="http://www.opengovguide.com/glossary">Open Government Guide</a>.</p>
            </div>

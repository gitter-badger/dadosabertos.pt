            <div class="col-sm-6 col-md-6">
              <p>Os dados abertos (ou open data em inglês) são dados que podem ser livremente usados, reutilizados e redistribuídos por qualquer pessoa - sujeitos, no máximo, à exigência de atribuição da fonte e partilha segundo os mesmos termos.</p>
              <p>Quando falamos de dados, estamos a referir-nos a conjuntos de informação estruturada; na gíria técnica, referimo-nos a datasets ou bases de dados. São bons exemplos os censos populacionais, registos meteorológicos, registos de empresas ou transcrições parlamentares.</p>
            </div>
            <div class="col-sm-6 col-md-6">
              <p>Os Estados, Parlamentos e inúmeras instituições públicas produzem conjuntos de dados de enorme valor. Na maior parte dos casos, tratam-se de informação pública destinada a ser disseminada.</p>
              <p>No entanto, muitas vezes os termos do acesso a esses dados são pouco claros ou incluem restrições que chocam com os princípios do interesse público e da transparência elementar.</p>
              <p>Daí a necessidade de uma definição clara e objetiva, proposta inicialmente pela <a href="https://okfn.org/">Open Knowledge International</a> no <a href="http://opendefinition.org">OpenDefinition.org</a>, de princípios que possam assegurar o direito do acesso à informação pública.</p>É essa a agenda open data.</p>
